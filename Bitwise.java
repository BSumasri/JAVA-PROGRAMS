class Bitwise{
public static void main(String arg[])
{

int num1 =20;
int num2 =30;
System.out.println("Bitwise AND:"+(num1 & num2));
System.out.println("Bitwise OR:"+(num1|num2));
System.out.println("Bitwise XOR:"+(num1^num2));
System.out.println("Bitwise 1'S Compliment:"+(~num2));
System.out.println("Bitwise shift left:"+(num2<<2));
System.out.println("Bitwise  shift right:"+(num1>>2));







}
}